        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model, HttpPostedFileBase upload)
        {

            try
            {
                if (ModelState.IsValid)
                {
                    var user = new ApplicationUser { UserName = model.Email, Email = model.Email, Name = model.Name, City = model.City, Gender = model.Genders, BirthDate = model.Birthdate };
                    var profile = new Profiles();
                    db.Profiles.Add(profile);
                    var ids = model.Interests.Where(x => x.Checked).Select(x => x.Id).ToList();
                    var interestes = db.Interests.Where(x => ids.Contains(x.Id)).ToList();
                    foreach (var item in interestes)
                    {
                        item.Profiles.Add(profile);
                    }

                    profile.Description = model.Description;
                    user.Profile = profile;


                    if (upload != null && upload.ContentLength > 0)
                    {
                        using (var reader = new BinaryReader(upload.InputStream))
                        {
                            profile.Picture = reader.ReadBytes(upload.ContentLength);
                        }
                    }
                    var u = new ApplicationUserManager(new UserStore<ApplicationUser>(db));
                    var result = await u.CreateAsync(user, model.Password);
                    if (result.Succeeded)
                    {
                        await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                        // For more information on how to enable account confirmation and password reset please visit https://go.microsoft.com/fwlink/?LinkID=320771
                        // Send an email with this link
                        // string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                        // var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                        // await UserManager.SendEmailAsync(user.Id, "Confirm your account", "Please confirm your account by clicking <a href=\"" + callbackUrl + "\">here</a>");
                        db.SaveChanges();
                        return RedirectToAction("Index", "Home");
                    }

                    AddErrors(result);

                }

                var interests = db.Interests.Select(x => x).ToList();
                model.Interests = interests;

                return View(model);
            }
            catch (Exception)
            {

                return RedirectToAction("CatchError", "Base");
            }

        }

        //
        // GET: /Account/ConfirmEmail
        [AllowAnonymous]
        public async Task<ActionResult> ConfirmEmail(string userId, string code)
        {

            try
            {
                if (userId == null || code == null)
                {
                    return View("Error");
                }
                var result = await UserManager.ConfirmEmailAsync(userId, code);
                return View(result.Succeeded ? "ConfirmEmail" : "Error");
            }
            catch (Exception)
            {

                return RedirectToAction("CatchError", "Base");
            }

        }

